                <h4 class="serendipity_title"><a href="/a/114-trigami-Affiliate-Programm.html">trigami Affiliate-Programm</a></h4>

        <div class="serendipity_entry serendipity_entry_author_Blogaddict ">
                        <span class="serendipity_entryIcon">
                                                    </span>
            
            <div class="serendipity_entry_body">
                <a href="http://www.trigami.com/?blog=http://www.blogaddict.de/">Trigami</a> hat vor kurzem ein Partnerprogramm gestartet und bezahlt eine Provision von 10 EUR für neue Blogger sobald diese die erste Rezension erfolgreich abgegeben haben.<br />Das Trackingverfahren ist dabei sehr fair gelöst: Zum einen wird ein Cookie mit einer Laufzeit von einem Jahr gesetzt, zum anderen werden auch Referer ausgewertet. Die Auswertung der Sales ist bisher wenig automatisiert, vielmehr erhält man einfach eine Email sobald etwas zur Auszahlung ansteht.<br />Besonders interessant: Auch vergangene Empfehlungen möchte Trigami anhand von Logfile-Analysen noch den verweisenden Blogs zuweisen. Damit sollte es sich für jeden Blogger der einmal direkt zu Trigami gelinkt hat lohnen, sich jetzt noch auf der Plattform anzumelden.<br />
            </div>

            
            
