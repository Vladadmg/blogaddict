                <h4 class="serendipity_title"><a href="/a/108-Amazon-Self-Optimizing-Links-gleichzeitig-mit-Google-Adsense.html">Amazon Self-Optimizing Links gleichzeitig mit Google Adsense</a></h4>

        <div class="serendipity_entry serendipity_entry_author_Blogaddict ">
            
            <div class="serendipity_entry_body">
                <br />
<p>Amazon hat heute im Affiliate-Blog einen <a href="http://affiliate-blog.amazon.de/2007/02/wir_erhalten_ve.html" target="_blank">Kommentar zur gleichzeitigen Platzierung von Google Adsense und kontext-sensitiven Werbemitteln von Amazon wie den Self-Optimizing Links veröffentlicht</a>. Aus Sicht von Amazon verstößt eine gleichzeitige Platzierung beider Werbemittel nicht gegen die Richtlinien des Adsense-Programmes, da Amazon kein Werbenetzwerk und damit kein Wettbewerber von Google Adsense ist.</p><p>Ein Blick in die <a href="https://www.google.com/adsense/support/bin/answer.py?answer=48182&amp;sourceid=aso&amp;subid=ww-ww-et-asui&amp;medium=link&amp;hl=de#com" target="_blank">Programmrichtlinien von Google Adsense</a> lässt aber schnell wieder Unsicherheit aufkommen:</p><br />
  <pre>Um Verwirrung bei den Nutzern zu<br />vermeiden, dürfen Google-Anzeigen oder -Suchfelder nicht auf Websites<br />mit anderen Anzeigen oder Services veröffentlicht werden, die dasselbe<br />Layout und dieselben Farben wie Google-Anzeigen oder -Suchfelder<br />verwenden. Auf Ihrer Website dürfen Sie zwar eigene Anzeigen verkaufen,<br />müssen jedoch dabei sicherstellen, dass diese Anzeigen keinesfalls wie<br />Imitationen von Google-Anzeigen aussehen. </pre><br />
  <p>Google schließt dort keinesfalls Wettbewerber aus, sondern vielmehr alle Dienste deren Anzeigen wie Google Adsense Anzeigen aussehen. Zumindestens auf eine deutliche farbliche Abhebung der Self-Optimizing Links gegenüber den Adsense-Anzeigen sollte man schon achten solange es von Google keine offizielle Reaktion zu dem Thema gibt.</p><br />

