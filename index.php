                <h4 class="serendipity_title"><a href="/a/105-Google-kennt-alle-.at-Domains.html">Google kennt alle .at Domains</a></h4>

        <div class="serendipity_entry serendipity_entry_author_Blogaddict ">
                        <span class="serendipity_entryIcon">
                                                    </span>
            
            <div class="serendipity_entry_body">
                <p>Google hat mit nic.at, den Verwaltern der österreichischen Toplevel-Domain .at, einen sogenannten Search Engine Visibility Contract geschlossen. Mit 7-tägiger Verzögerung bekommt Google alle neuen .at-Domains, Änderungen und Löschungen mitgeteilt. Laut nic.at ist .at die erste Country-TLD mit einem solchen Vertrag. Wichtig ist die Beschränkung auf Country-TLD, denn .com, .net und .org bekommt Google als eingetragener Registrar schon länger.</p><p>Wirklich geholfen ist Website-Betreibern damit aber eher nicht. Wie schnell sich Google eine Domain ansieht und neue Inhalte aufnimmt entscheidet am Ende immer noch ein Algorithmus der vor allem über die Menge der eingehenden Links die Wichtigkeit der Domain bestimmt.</p><p>(Quelle: <a href="http://www.internetmarketing-news.de/2007/02/21/google-wird-uber-alle-neuen-at-domains-informiert/" target="_blank">Internetmarketing News</a>)</p><br />

